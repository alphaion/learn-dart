void main() {

  // Switch Case Statements: Applicable for only "int" & "String"

  String grade = 'D';

  switch (grade) {

    case 'A':
      print("Excellent.");
      break;

    case 'B':
      print("Very Good.");
      break;
      
    case 'C':
      print("Good but work hard.");
      break;

    case 'F':
      print("You have failed.");
      break;

    default:
      print("Invalid Grade.");
  
  }

}